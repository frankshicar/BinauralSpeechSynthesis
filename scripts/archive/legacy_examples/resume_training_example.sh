#!/bin/bash

# ============================================================================
# 接續訓練範例腳本
# ============================================================================

# 設定變數
DATASET_DIR="./dataset/trainset"
OUTPUT_DIR="./outputs_with_ipd"
NUM_GPUS=1
BLOCKS=3
TOTAL_EPOCHS=100

# ============================================================================
# 範例 1: 檢查當前訓練狀態
# ============================================================================
echo "=========================================="
echo "檢查訓練狀態"
echo "=========================================="

# 列出所有 checkpoint
python check_checkpoint.py --artifacts_dir $OUTPUT_DIR --list

# 查看訓練歷史
python check_checkpoint.py --artifacts_dir $OUTPUT_DIR --history

# ============================================================================
# 範例 2: 從最後的 checkpoint 恢復訓練
# ============================================================================
echo ""
echo "=========================================="
echo "從最後的 checkpoint 恢復訓練"
echo "=========================================="

# 假設最後保存的是 epoch-40
LAST_EPOCH=40

echo "從 epoch-${LAST_EPOCH} 繼續訓練..."
python train.py \
    --dataset_directory $DATASET_DIR \
    --artifacts_directory $OUTPUT_DIR \
    --num_gpus $NUM_GPUS \
    --blocks $BLOCKS \
    --resume epoch-${LAST_EPOCH}

# ============================================================================
# 範例 3: 檢查特定 checkpoint 的詳細資訊
# ============================================================================
echo ""
echo "=========================================="
echo "檢查 checkpoint 詳細資訊"
echo "=========================================="

python check_checkpoint.py \
    --artifacts_dir $OUTPUT_DIR \
    --checkpoint epoch-${LAST_EPOCH}

# ============================================================================
# 範例 4: 監控訓練進度
# ============================================================================
echo ""
echo "=========================================="
echo "監控訓練進度"
echo "=========================================="

# 在另一個終端運行
# python monitor_training.py

# 或者可視化
# python visualize_training.py
